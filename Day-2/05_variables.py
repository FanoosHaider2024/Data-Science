# Rules of variables

# 1. Start variables with letter or underscore
# 2. Do not start with numbers
# 3. Do not use special characters other than underscore ( _ , @, #, $, %, etc.)
# 4. Variable names are case sensitive
# 5. Variable names should be short
# 6. Variable names should be descriptive
# 7. Variable names should not be same as keywords (e.g. if, else, for, while, etc.)
# 8. Variable names should not be same as functions
# 9. Variable names should not be same as classes
# 10. Variable names should not be same as modules
# 11. Space is not allowed in variable names
# 12. Global trends (df = dataframe)
# 13 Mathematical operations are not allowed in variable names (e.g. +, -, *, /, **, etc.)


